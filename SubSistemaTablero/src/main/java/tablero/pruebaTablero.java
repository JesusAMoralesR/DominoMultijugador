        componentesEnPantalla.replace(new Point(9, 4), label1);
        componentesEnPantalla.replace(new Point(9, 5), label1);

        PanelPrincipal.remove(espacio94);
        PanelPrincipal.remove(espacio95);

        PanelPrincipal.add(label1, gbc);
